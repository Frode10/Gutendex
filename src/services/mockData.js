// Mock data for a single book
export const getMockBook = (id) => {
  const book = mockBooks.results.find(b => b.id === parseInt(id));
  return book || mockBooks.results[0];
};

// Filter mock books by topic
export const filterMockByTopic = (topic) => {
  const filtered = mockBooks.results.filter(book =>
    book.subjects.some(subject =>
      subject.toLowerCase().includes(topic.toLowerCase())
    )
  );

  return {
    ...mockBooks,
    count: filtered.length,
    results: filtered.length > 0 ? filtered : mockBooks.results.slice(0, 5)
  };
};

// Filter mock books by search query
export const searchMockBooks = (query) => {
  const filtered = mockBooks.results.filter(book =>
    book.title.toLowerCase().includes(query.toLowerCase()) ||
    book.authors.some(author => author.name.toLowerCase().includes(query.toLowerCase()))
  );

  return {
    ...mockBooks,
    count: filtered.length,
    results: filtered.length > 0 ? filtered : []
  };
};
